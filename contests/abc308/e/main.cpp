int main() {
    int n; cin >> n;
    vector<int> a(n);
    for (int i=0; i<n; i++) cin >> a[i];
    string s; cin >> s;
    string t;
    for (int i=0; i<n; i++) {
        int x = a[i];
        if (s[i] == 'E') x += 3;
        if (s[i] == 'X') x += 6;
        t += '0' + x;
    }
    cout << solve<long long>(t) << endl;
}